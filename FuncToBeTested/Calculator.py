class Calculator:
    def add(self, a, b):
        return a+b

    def add1(self, a:int, b:int) ->int:
        return a+b

    def min(self, a, b):
        return a - b

    def multi(self, a, b):
        return a * b

    def div(self, a, b):
        return a / b